package com.mycompany.librarymanagementsystem;


public class MDate {
    String d, m, y;
    public MDate(String d, String m, String y){
        this.d=d;
        this.m=m;
        this.y=y;
    }
    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

    public String getY() {
        return y;
    }

    public void setY(String y) {
        this.y = y;
    }
 
}
